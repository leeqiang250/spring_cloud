package cn;

public enum  ResponseCode {

    SUCCESS("200", "success"),
    FAIL("500", "fail");

    private final String code;
    private final String desc;

    ResponseCode(String code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public String getCode() {
        return code;
    }

    public String getDesc() {
        return desc;
    }
}